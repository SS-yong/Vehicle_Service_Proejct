drop view if exists history;
create view history as
	select A.user_id, A.veh_id,A.ren_num, A.ren_stime, A.ren_splace, B.rtn_num, B.rtn_etime, B.rtn_eplace, B.rtn_dist, B.rtn_price, C.res_num, C.res_time
	from rentaltbl A join returntbl B on A.ren_num = B.rtn_num left join reservetbl C on A.res_num = C.res_num;
    
select * from history;





use project;

-- 날짜별 매출 분석 
select DATE_FORMAT(ren_stime, '%Y-%m-%d') as ren_date, 
format(sum(rtn_price),'C') as revenue
from history
group by ren_date;


-- 최다 대여지역 top 5
select ren_splace, count(*) as count_ren
from history
group by ren_splace
order by count_ren desc limit 5;

-- 최다 반납지역 top 5
select rtn_eplace, count(*) as count_rtn
from history
group by rtn_eplace
order by count_rtn desc limit 5;


-- 방치 유저, 기기, 반납 시간, 장소 명단
select user_id, veh_id, rtn_etime, rtn_eplace
from history
where rtn_eplace not in 
(select place_name from placetbl);


-- 요일별, 기기 종류별 대여 횟수
select a.ren_date,bike, kick
from (select DATE_FORMAT(ren_stime, '%Y-%m-%d') as ren_date, count(*) as bike
from history
where veh_id < 200
group by ren_date) as a
join
(select DATE_FORMAT(ren_stime, '%Y-%m-%d') as ren_date, count(*) as kick
from history
where veh_id >= 200
group by ren_date) as b
on a.ren_date = b.ren_date;
